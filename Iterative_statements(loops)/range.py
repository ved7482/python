# Python range() Function

# Range from 0 to 4 (not including 5)
for i in range(5):
    print(i)
print("\n")

# Range from 3 to 7 (not including 8)
for i in range(3, 8):
    print(i)
print("\n")

# Range from 2 to 10 with a step of 2(will add 2 after each step)
#can be ued for printing tables, odd no., even no., etc.
for i in range(2, 11, 2):
    print(i)
print("\n")

#Range from 10 to 1 
for i in range(10,0,-1):
    print(i)